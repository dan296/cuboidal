function getRandomWords(array, numWords, wordException = '') {
    // Filter out the wordException
    const filteredArray = array.filter(word => word !== wordException);
    // Fisher-Yates shuffle algorithm
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array.slice(0, numWords);
}

function findRandomWordStartingWith(words, letter, existingWords) {
    const matchingWords = words.filter(word => word[0].toLowerCase() === letter.toLowerCase());
    if (matchingWords.length === 0) return null;
    const randomIndex = Math.floor(Math.random() * matchingWords.length);
    return matchingWords[randomIndex];
}

function findRandomWordEndingWith(words, letter, existingWords) {
    const matchingWords = words.filter(word => word[word.length - 1].toLowerCase() === letter.toLowerCase());
    if (matchingWords.length === 0) return null;
    const randomIndex = Math.floor(Math.random() * matchingWords.length);
    return matchingWords[randomIndex];
}

function findRandomWordStartingWithAndEndingWith(words, startletter, endletter, existingWords) {
    const matchingWords = words.filter(word => word[0].toLowerCase() === startletter.toLowerCase() && word[word.length - 1].toLowerCase() === endletter.toLowerCase());
    if (matchingWords.length === 0) return null;
    const randomIndex = Math.floor(Math.random() * matchingWords.length);
    return matchingWords[randomIndex];
}

